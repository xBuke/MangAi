  function getLang() {
    try {
      var stored = localStorage.getItem('demo_lang');
      if (stored === 'en' || stored === 'hr') return stored;
    } catch (e) {}
    return 'hr';
  }

  function setLang(lang) {
    try {
      localStorage.setItem('demo_lang', lang);
    } catch (e) {}
  }

  function getContactUrl(lang) {
    return lang === 'en' ? CONTACT_EN : CONTACT_HR;
  }

  function tCommon(key, lang) {
    lang = lang || getLang();
    return (t[lang] && t[lang].common && t[lang].common[key]) || t.hr.common[key] || key;
  }

  function tVertical(vertical, key, lang) {
    lang = lang || getLang();
    var v = t[lang] && t[lang].verticals && t[lang].verticals[vertical];
    return (v && v[key]) || (t.hr.verticals[vertical] && t.hr.verticals[vertical][key]) || key;
  }

